package nari.parameter.bean;

public class SelfDefField {

	private String selfFieldName = "";

	private String selfFieldValue = "";
	
	private String selfFieldAlias = "";

	public String getSelfFieldName() {
		return selfFieldName;
	}

	public void setSelfFieldName(String selfFieldName) {
		this.selfFieldName = selfFieldName;
	}

	public String getSelfFieldValue() {
		return selfFieldValue;
	}

	public void setSelfFieldValue(String selfFieldValue) {
		this.selfFieldValue = selfFieldValue;
	}

	public String getSelfFieldAlias() {
		return selfFieldAlias;
	}

	public void setSelfFieldAlias(String selfFieldAlias) {
		this.selfFieldAlias = selfFieldAlias;
	}
	
	
	
}
